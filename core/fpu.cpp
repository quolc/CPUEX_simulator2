#include <cmath>

float fadd(float a, float b) {
    return a + b;
}

float fsub(float a, float b) {
    return a - b;
}

float fmul(float a, float b) {
    return a * b;
}

float finv(float a) {
    return 1 / a;
}

float fsqr(float a) {
    return sqrt(a);
}

float fneg(float a) {
    return -a;
}
